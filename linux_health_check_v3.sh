generate_recommendations() {
    section_header "14" "ALERT SUMMARY & RECOMMENDATIONS"
    echo "================================================================================"
    echo ""

    if [[ ! -s "${ALERT_LOG}" ]]; then
        echo "  **********************************************"
        echo "  * ALL SYSTEMS HEALTHY - NO BREACHES FOUND   *"
        echo "  **********************************************"
        echo ""
        return
    fi

    local critical_count warning_count info_count
    critical_count=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    warning_count=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)
    info_count=$(grep -c "^INFO" "${ALERT_LOG}" 2>/dev/null || echo 0)

    echo "  Overall: ${critical_count} CRITICAL | ${warning_count} WARNING | ${info_count} INFO"
    echo ""

    # Findings table
    local seq_num=1
    printf "  %-4s %-10s %-18s %s\n" "#" "SEVERITY" "COMPONENT" "FINDING"
    echo "  ========================================================================================"

    while IFS='|' read -r severity component message recommendation; do
        printf "  %-4s %-10s %-18s %s\n" "$seq_num" "$severity" "$component" "${message:0:70}"
        seq_num=$((seq_num + 1))
    done < "${ALERT_LOG}"

    # Recommendations
    echo ""
    echo "  RECOMMENDATIONS:"
    echo "  ========================================================================================"
    seq_num=1
    while IFS='|' read -r severity component message recommendation; do
        printf "  %d. [%s][%s] %s\n" "$seq_num" "$severity" "$component" "$recommendation"
        seq_num=$((seq_num + 1))
    done < "${ALERT_LOG}"

    # Team notification mapping
    echo ""
    echo "  TEAM NOTIFICATION MAP:"
    echo "  ========================================================================================"
    local -A team_map
    while IFS='|' read -r severity component message recommendation; do
        case "$component" in
            ORACLE*) team_map["Oracle-DBA-Team"]=1 ;;
            S3FS*|CLOUD*) team_map["Cloud-AWS-Team"]=1 ;;
            NFS*) team_map["Network-Team"]=1; team_map["Storage-Team"]=1 ;;
            DISK*) team_map["Storage-VMware-Team"]=1 ;;
            SSH*|SUID*|BRUTE*|SELINUX*|FIREWALL*|CERT*|UID0*|EMPTY-PASSWORD*|WORLD-WRITABLE*|AUDIT*|CIS*|VAPT*|PWQUALITY*|PASSWORD*) team_map["Security-Team"]=1 ;;
            BACKUP*) team_map["Backup-Team"]=1 ;;
            PATCHING*|SECURITY-PATCHES*|SUBSCRIPTION*) team_map["Patch-Management-Team"]=1 ;;
            *) team_map["Application-Team"]=1; team_map["Unix-Admin-Team"]=1 ;;
        esac
    done < "${ALERT_LOG}"

    for team in "${!team_map[@]}"; do
        echo "  --> NOTIFY: ${team}"
    done
    echo ""
}

