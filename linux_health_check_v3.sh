#=============================================================================
# UTILITY FUNCTIONS
#=============================================================================
log_alert() {
    echo "${1}|${2}|${3}|${4}" >> "${ALERT_LOG}"
}

check_command() {
    command -v "$1" &>/dev/null
}

pct_bar() {
    local pct=${1%%.*}
    [[ -z "$pct" || "$pct" == "N/A" ]] && pct=0
    local filled=$((pct / 5))
    local empty=$((20 - filled))
    printf '[%s%s] %3d%%' "$(printf '#%.0s' $(seq 1 $filled 2>/dev/null))" \
           "$(printf '.%.0s' $(seq 1 $empty 2>/dev/null))" "$pct"
}

get_sar_file() {
    local day="$1"
    local sar_bin="${SAR_DIR}/sa${day}"
    [[ -f "$sar_bin" ]] && echo "$sar_bin" || echo ""
}

divider() {
    echo "--------------------------------------------------------------------------------"
}

section_header() {
    echo ""
    echo "[$1] $2"
    divider
}

#=============================================================================
# SECTION 0: PRE-FLIGHT DEPENDENCY CHECK (warn only, no auto-install)
#=============================================================================
check_dependencies() {
    echo "================================================================================"
    echo "  PRE-FLIGHT: DEPENDENCY CHECK"
    echo "================================================================================"
    echo ""

    local missing=0
    declare -A DEPS=(
        ["sysstat"]="sar iostat mpstat (historical analysis)"
        ["bc"]="threshold calculations"
        ["nfs-utils"]="nfsstat nfsiostat (NFS health)"
        ["lsof"]="open port / file handle analysis"
        ["net-tools"]="netstat (legacy network checks)"
        ["iproute"]="ss (socket statistics)"
        ["audit"]="auditd (audit daemon health)"
        ["aide"]="file integrity monitoring"
        ["openssl"]="certificate expiry checks"
    )

    printf "  %-20s %-10s %s\n" "PACKAGE" "STATUS" "PURPOSE"
    divider

    for pkg in sysstat bc nfs-utils lsof net-tools iproute audit aide openssl; do
        if rpm -q "$pkg" &>/dev/null; then
            printf "  %-20s %-10s %s\n" "$pkg" "[OK]" "${DEPS[$pkg]}"
        else
            printf "  %-20s %-10s %s\n" "$pkg" "[MISSING]" "${DEPS[$pkg]}"
            missing=$((missing + 1))
            log_alert "WARNING" "DEPENDENCY" "Package '${pkg}' not installed - ${DEPS[$pkg]}" \
                "Install: yum install -y ${pkg} (or) dnf install -y ${pkg}"
        fi
    done

    # Check individual commands even if package names differ
    echo ""
    echo "  Command availability:"
    for cmd in sar iostat mpstat bc ss lsof auditctl aide openssl mail salt-call; do
        if check_command "$cmd"; then
            printf "  %-20s [AVAILABLE]\n" "$cmd"
        else
            printf "  %-20s [NOT FOUND]\n" "$cmd"
        fi
    done

    if (( missing > 0 )); then
        echo ""
        echo "  *** WARNING: ${missing} package(s) missing. Some checks will be skipped. ***"
        echo "  *** Install missing packages to enable full health check coverage.       ***"
    fi
    echo ""
}

