# SECTION 8: S3FS MOUNT STATISTICS
#=============================================================================
collect_s3fs_stats() {
    section_header "8" "S3FS MOUNT STATISTICS"

    local s3fs_mounts
    s3fs_mounts=$(mount -t fuse.s3fs 2>/dev/null || grep s3fs /proc/mounts 2>/dev/null)
    if [[ -z "$s3fs_mounts" ]]; then
        echo "     No S3FS mounts detected."
        echo ""
        return
    fi

    echo "$s3fs_mounts" | while read src mnt fstype opts rest; do
        printf "     Source: %-30s  Mount: %-20s\n" "$src" "$mnt"
        if timeout 10 stat "$mnt" &>/dev/null; then
            echo "     Status: RESPONSIVE"
            df -hP "$mnt" 2>/dev/null | tail -1 | while read fs size used avail pct mount; do
                printf "     Size: %-8s  Used: %-8s  Available: %-8s  Usage: %s\n" "$size" "$used" "$avail" "$pct"
            done
        else
            echo "     Status: *** NOT RESPONDING ***"
            log_alert "CRITICAL" "S3FS" "S3FS mount ${mnt} not responding" \
                "Check AWS credentials, network, s3fs process. Engage Cloud team."
        fi
        echo ""
    done
}

#=============================================================================
# SECTION 9: SAR HISTORICAL ANALYSIS (LAST 24 HOURS)
#=============================================================================
collect_historical_stats() {
    section_header "9" "HISTORICAL ANALYSIS - LAST 24 HOURS (SAR DATA)"
    echo "================================================================================"

    local sar_yesterday sar_today
    sar_yesterday=$(get_sar_file "$YESTERDAY")
    sar_today=$(get_sar_file "$TODAY")

    if [[ -z "$sar_yesterday" && -z "$sar_today" ]]; then
        echo "  *** SAR data not available. Ensure sysstat is installed and sa1/sa2 are running. ***"
        log_alert "WARNING" "SAR-DATA" "SAR data files not found in ${SAR_DIR}" \
            "Install/enable sysstat: yum install sysstat && systemctl enable --now sysstat"
        echo ""
        return
    fi

    # ---- 9a: CPU Historical ----
    echo ""
    echo "  [9a] CPU Usage History (sar -u)"
    echo "  -----------------------------------------------"
    for sar_file in "$sar_yesterday" "$sar_today"; do
        [[ -z "$sar_file" || ! -f "$sar_file" ]] && continue
        local day_label
        [[ "$sar_file" == *"sa${YESTERDAY}" ]] && day_label="YESTERDAY" || day_label="TODAY"
        echo "  -- ${day_label} --"
        sar -u -f "$sar_file" 2>/dev/null | grep -v "^$\|Linux\|Average\|%idle\|RESTART" | \
        awk -v thresh="$THRESH_CPU" '{
            idle=$NF; cpu=100-idle;
            if(cpu>max) max=cpu
            if(cpu>thresh) { printf "     BREACH: %s - CPU %.1f%%\n", $1, cpu; count++ }
        } END {
            printf "     Peak CPU: %.1f%%\n", max
            if(count>0) printf "     Total breaches: %d intervals\n", count
        }'
        echo ""
    done

    # ---- 9b: Memory Historical ----
    echo "  [9b] Memory Usage History (sar -r)"
    echo "  -----------------------------------------------"
    for sar_file in "$sar_yesterday" "$sar_today"; do
        [[ -z "$sar_file" || ! -f "$sar_file" ]] && continue
        local day_label
        [[ "$sar_file" == *"sa${YESTERDAY}" ]] && day_label="YESTERDAY" || day_label="TODAY"
        echo "  -- ${day_label} --"
        sar -r -f "$sar_file" 2>/dev/null | grep -v "^$\|Linux\|RESTART" | \
        awk -v thresh="$THRESH_MEMORY" '
        /Average/ { printf "     Average MemUsed%%: %s\n", $5; next }
        /^[0-9]/ {
            if($5+0 > thresh) { printf "     BREACH: %s - MemUsed: %s%%\n", $1, $5; count++ }
            if($5+0 > max) max=$5+0
        } END { printf "     Peak MemUsed%%: %.1f%%\n", max; if(count>0) printf "     Breaches: %d\n", count }'
        echo ""
    done

    # ---- 9c: Swap Historical ----
    echo "  [9c] Swap Usage History (sar -S)"
    echo "  -----------------------------------------------"
    for sar_file in "$sar_yesterday" "$sar_today"; do
        [[ -z "$sar_file" || ! -f "$sar_file" ]] && continue
        local day_label
        [[ "$sar_file" == *"sa${YESTERDAY}" ]] && day_label="YESTERDAY" || day_label="TODAY"
        echo "  -- ${day_label} --"
        sar -S -f "$sar_file" 2>/dev/null | grep -v "^$\|Linux\|RESTART" | \
        awk -v thresh="$THRESH_SWAP" '
        /Average/ { printf "     Average SwapUsed%%: %s\n", $5; next }
        /^[0-9]/ {
            if($5+0 > thresh) { printf "     BREACH: %s - SwapUsed: %s%%\n", $1, $5; count++ }
            if($5+0 > max) max=$5+0
        } END { printf "     Peak SwapUsed%%: %.1f%%\n", max; if(count>0) printf "     Breaches: %d\n", count }'
        echo ""
    done

    # ---- 9d: Disk I/O Historical ----
    echo "  [9d] Disk I/O History (sar -d)"
    echo "  -----------------------------------------------"
    for sar_file in "$sar_yesterday" "$sar_today"; do
        [[ -z "$sar_file" || ! -f "$sar_file" ]] && continue
        local day_label
        [[ "$sar_file" == *"sa${YESTERDAY}" ]] && day_label="YESTERDAY" || day_label="TODAY"
        echo "  -- ${day_label} --"
        sar -d -f "$sar_file" 2>/dev/null | grep -v "^$\|Linux\|RESTART" | \
        awk -v ut="$THRESH_DISK_UTIL" -v at="$THRESH_DISK_AWAIT" '
        /Average/ { printf "     Avg Dev:%-12s await:%s %%util:%s\n", $2, $(NF-1), $NF; next }
        /^[0-9]/ {
            if($NF+0 > ut) { printf "     BREACH[UTIL]: %s Dev:%-12s %%util:%s\n", $1, $2, $NF; uc++ }
            if($(NF-1)+0 > at) { printf "     BREACH[AWAIT]: %s Dev:%-12s await:%sms\n", $1, $2, $(NF-1); ac++ }
        } END { if(uc>0) printf "     Util breaches: %d\n", uc; if(ac>0) printf "     Await breaches: %d\n", ac }'
        echo ""
    done

    # ---- 9e: Network Errors ----
    echo "  [9e] Network Error Summary (sar -n EDEV)"
    echo "  -----------------------------------------------"
    for sar_file in "$sar_yesterday" "$sar_today"; do
        [[ -z "$sar_file" || ! -f "$sar_file" ]] && continue
        local day_label
        [[ "$sar_file" == *"sa${YESTERDAY}" ]] && day_label="YESTERDAY" || day_label="TODAY"
        echo "  -- ${day_label} --"
        sar -n EDEV -f "$sar_file" 2>/dev/null | grep "Average" | \
        awk '{ if($3+0>0 || $4+0>0 || $5+0>0 || $6+0>0)
            printf "     %-12s rxerr:%-6s txerr:%-6s coll:%-6s drop:%-6s\n", $2,$3,$4,$5,$6 }'
        echo ""
    done

    # ---- 9f: Consolidated Summary ----
    echo "  [9f] CONSOLIDATED 24-HOUR SUMMARY TABLE"
    echo "  ============================================================"
    printf "  %-20s %-15s %-15s %-10s\n" "METRIC" "PEAK" "THRESHOLD" "STATUS"
    echo "  ---------------------------------------------------------------"
    local summary_file="${sar_today:-$sar_yesterday}"
    if [[ -n "$summary_file" && -f "$summary_file" ]]; then
        local pk_cpu=$(sar -u -f "$summary_file" 2>/dev/null | grep -v "^$\|Linux\|Average\|RESTART" | \
            awk '{c=100-$NF; if(c>m) m=c} END{printf "%.1f",m}')
        local pk_mem=$(sar -r -f "$summary_file" 2>/dev/null | grep -v "^$\|Linux\|Average\|RESTART" | \
            awk '/^[0-9]/{if($5+0>m) m=$5+0} END{printf "%.1f",m}')
        local pk_swp=$(sar -S -f "$summary_file" 2>/dev/null | grep -v "^$\|Linux\|Average\|RESTART" | \
            awk '/^[0-9]/{if($5+0>m) m=$5+0} END{printf "%.1f",m}')

        local s1="OK"; (( $(echo "$pk_cpu > $THRESH_CPU" | bc -l 2>/dev/null || echo 0) )) && s1="BREACH"
        local s2="OK"; (( $(echo "$pk_mem > $THRESH_MEMORY" | bc -l 2>/dev/null || echo 0) )) && s2="BREACH"
        local s3="OK"; (( $(echo "$pk_swp > $THRESH_SWAP" | bc -l 2>/dev/null || echo 0) )) && s3="BREACH"

        printf "  %-20s %-15s %-15s %-10s\n" "CPU %" "${pk_cpu}%" "${THRESH_CPU}%" "$s1"
        printf "  %-20s %-15s %-15s %-10s\n" "Memory %" "${pk_mem}%" "${THRESH_MEMORY}%" "$s2"
        printf "  %-20s %-15s %-15s %-10s\n" "Swap %" "${pk_swp}%" "${THRESH_SWAP}%" "$s3"
    fi
    echo ""
}

#=============================================================================
# SECTION 10: SECURITY & VAPT AUDIT
#=============================================================================
collect_security_vapt() {
    section_header "10" "SECURITY & VAPT AUDIT"
    echo "================================================================================"

    # ---- 10a: SSH Security ----
    echo ""
    echo "  [10a] SSH Configuration Audit (/etc/ssh/sshd_config)"
    echo "  -----------------------------------------------"
    local sshd_conf="/etc/ssh/sshd_config"
    if [[ -f "$sshd_conf" ]]; then
        declare -A SSH_CHECKS=(
            ["PermitRootLogin"]="no"
            ["PasswordAuthentication"]="no"
            ["PermitEmptyPasswords"]="no"
            ["X11Forwarding"]="no"
            ["MaxAuthTries"]="4"
            ["ClientAliveInterval"]="300"
            ["ClientAliveCountMax"]="3"
            ["Protocol"]="2"
            ["LoginGraceTime"]="60"
            ["AllowAgentForwarding"]="no"
            ["Banner"]="/etc/issue.net"
        )

        printf "     %-30s %-15s %-15s %-10s\n" "PARAMETER" "CURRENT" "EXPECTED" "STATUS"
        echo "     -----------------------------------------------------------------------"

        for param in "${!SSH_CHECKS[@]}"; do
            local expected="${SSH_CHECKS[$param]}"
            local current
            current=$(grep -i "^${param}" "$sshd_conf" 2>/dev/null | tail -1 | awk '{print $2}')
            if [[ -z "$current" ]]; then
                current="(default)"
            fi

            local status="OK"
            if [[ "$current" == "(default)" ]]; then
                status="REVIEW"
                log_alert "WARNING" "SSH-CONFIG" "SSH ${param} not explicitly set (using default)" \
                    "Explicitly set ${param} ${expected} in sshd_config for compliance."
            elif [[ "$current" != "$expected" ]]; then
                status="NON-COMPLIANT"
                log_alert "CRITICAL" "SSH-SECURITY" "SSH ${param}=${current} (expected: ${expected})" \
                    "VAPT Finding: Set ${param} to ${expected} in /etc/ssh/sshd_config and restart sshd."
            fi
            printf "     %-30s %-15s %-15s %-10s\n" "$param" "$current" "$expected" "$status"
        done

        # Weak cipher check
        echo ""
        echo "  >> Weak Cipher/MAC Check:"
        local weak_ciphers="3des-cbc arcfour blowfish-cbc cast128-cbc"
        local configured_ciphers
        configured_ciphers=$(grep -i "^Ciphers" "$sshd_conf" 2>/dev/null | awk '{print $2}')
        if [[ -z "$configured_ciphers" ]]; then
            echo "     Ciphers not explicitly restricted (uses system defaults) - REVIEW NEEDED"
            log_alert "WARNING" "SSH-CIPHERS" "SSH Ciphers not explicitly restricted in sshd_config" \
                "Define strong cipher list in sshd_config. Remove CBC mode ciphers per VAPT requirements."
        else
            local found_weak=0
            for wc in $weak_ciphers; do
                if echo "$configured_ciphers" | grep -qi "$wc"; then
                    echo "     *** WEAK CIPHER FOUND: ${wc} ***"
                    found_weak=1
                fi
            done
            (( found_weak == 0 )) && echo "     No weak ciphers in configuration."
        fi
    else
        echo "     /etc/ssh/sshd_config not found."
    fi

    # ---- 10b: Failed Login / Brute Force ----
    echo ""
    echo "  [10b] Failed Login Analysis (last 24 hours)"
    echo "  -----------------------------------------------"
    local secure_log="/var/log/secure"
    [[ ! -f "$secure_log" ]] && secure_log="/var/log/auth.log"

    if [[ -f "$secure_log" ]]; then
        local yesterday_date=$(date -d "yesterday" '+%b %e')
        local today_date=$(date '+%b %e')

        local total_failures
        total_failures=$(grep -cE "Failed password|authentication failure" "$secure_log" 2>/dev/null || echo 0)
        echo "     Total failed login attempts (in log): ${total_failures}"

        echo ""
        echo "     Top Source IPs with Failed Logins:"
        printf "     %-20s %-10s %s\n" "SOURCE IP" "ATTEMPTS" "STATUS"
        echo "     ------------------------------------------------"
        grep "Failed password" "$secure_log" 2>/dev/null | \
            grep -oE "([0-9]{1,3}\.){3}[0-9]{1,3}" | sort | uniq -c | sort -rn | head -10 | \
            while read count ip; do
                local status="OK"
                if (( count >= THRESH_FAILED_LOGIN )); then
                    status="BRUTE-FORCE?"
                    log_alert "CRITICAL" "BRUTE-FORCE" "IP ${ip} has ${count} failed login attempts" \
                        "Potential brute force attack from ${ip}. Block via firewall. Notify Security team."
                fi
                printf "     %-20s %-10s %s\n" "$ip" "$count" "$status"
            done

        echo ""
        echo "     Failed Login Usernames (top 10):"
        grep "Failed password" "$secure_log" 2>/dev/null | \
            grep -oP "for (invalid user )?\K\S+" | sort | uniq -c | sort -rn | head -10 | \
            while read count user; do
                printf "     %-20s %s attempts\n" "$user" "$count"
            done

        # Locked accounts
        echo ""
        echo "     Locked Accounts (pam_faillock):"
        if check_command faillock; then
            local locked_users
            locked_users=$(faillock --dir /var/run/faillock 2>/dev/null | grep -v "^$\|When" | awk '{print $1}' | sort -u)
            if [[ -n "$locked_users" ]]; then
                echo "$locked_users" | while read u; do
                    echo "     *** LOCKED: ${u} ***"
                    log_alert "WARNING" "ACCOUNT-LOCKED" "Account '${u}' is locked by faillock" \
                        "Verify if legitimate lockout. Unlock with: faillock --user ${u} --reset"
                done
            else
                echo "     No accounts currently locked."
            fi
        else
            echo "     faillock command not available."
        fi
    else
        echo "     Secure log not found at /var/log/secure or /var/log/auth.log"
    fi

    # ---- 10c: User & Privilege Audit ----
    echo ""
    echo "  [10c] User & Privilege Audit"
    echo "  -----------------------------------------------"

    # UID 0 accounts
    echo "     UID 0 Accounts (should only be root):"
    local uid0_users
    uid0_users=$(awk -F: '$3==0 {print $1}' /etc/passwd)
    echo "$uid0_users" | while read u; do
        if [[ "$u" != "root" ]]; then
            echo "     *** NON-COMPLIANT: ${u} has UID 0 ***"
            log_alert "CRITICAL" "UID0-USER" "Non-root account '${u}' has UID 0" \
                "VAPT Critical: Remove UID 0 from account '${u}'. Only root should have UID 0."
        else
            echo "     root (expected)"
        fi
    done

    # Empty password accounts
    echo ""
    echo "     Accounts with Empty Passwords:"
    local empty_pw
    empty_pw=$(awk -F: '($2=="" || $2=="!!" || $2=="!") && $1!="*" {print $1}' /etc/shadow 2>/dev/null)
    if [[ -n "$empty_pw" ]]; then
        echo "$empty_pw" | while read u; do
            echo "     *** ${u} - EMPTY/NO PASSWORD ***"
            log_alert "CRITICAL" "EMPTY-PASSWORD" "Account '${u}' has empty or no password" \
                "VAPT Critical: Set strong password for account '${u}' immediately."
        done
    else
        echo "     None found."
    fi

    # Accounts with no expiry
    echo ""
    echo "     Accounts with No Password Expiry (non-system, UID>=1000):"
    awk -F: '$3>=1000 && $1!="nobody" && $1!="nfsnobody" {print $1}' /etc/passwd 2>/dev/null | while read u; do
        local max_days
        max_days=$(chage -l "$u" 2>/dev/null | grep "Maximum" | awk -F: '{print $2}' | tr -d ' ')
        if [[ "$max_days" == "99999" || "$max_days" == "-1" || "$max_days" == "never" ]]; then
            printf "     %-20s Max Days: %-10s *** NO EXPIRY ***\n" "$u" "$max_days"
            log_alert "WARNING" "PASSWORD-EXPIRY" "Account '${u}' has no password expiry (max_days: ${max_days})" \
                "Set password expiry: chage -M ${CIS_PASS_MAX_DAYS} ${u}"
        fi
    done

    # NOPASSWD in sudoers
    echo ""
    echo "     NOPASSWD Entries in Sudoers:"
    local nopasswd_entries
    nopasswd_entries=$(grep -rh "NOPASSWD" /etc/sudoers /etc/sudoers.d/ 2>/dev/null | grep -v "^#")
    if [[ -n "$nopasswd_entries" ]]; then
        echo "$nopasswd_entries" | while read line; do
            echo "     *** ${line} ***"
        done
        log_alert "WARNING" "SUDOERS-NOPASSWD" "NOPASSWD entries found in sudoers configuration" \
            "VAPT Finding: Remove NOPASSWD from sudoers. All sudo must require password for audit compliance."
    else
        echo "     None found (compliant)."
    fi

    # Orphan users (UID>=1000, no valid home)
    echo ""
    echo "     Orphan Users (no home directory):"
    awk -F: '$3>=1000 && $1!="nobody" && $1!="nfsnobody" {print $1":"$6}' /etc/passwd 2>/dev/null | while IFS=: read u home; do
        if [[ ! -d "$home" ]]; then
            printf "     %-20s Home: %-30s *** MISSING ***\n" "$u" "$home"
            log_alert "WARNING" "ORPHAN-USER" "User '${u}' home directory '${home}' does not exist" \
                "Review account '${u}'. Remove if unused: userdel ${u}"
        fi
    done

    # Recently modified passwd/shadow
    echo ""
    echo "     Recently Modified Auth Files (last 7 days):"
    for f in /etc/passwd /etc/shadow /etc/group /etc/gshadow /etc/sudoers; do
        if [[ -f "$f" ]]; then
            local mod_days
            mod_days=$(( ($(date +%s) - $(stat -c %Y "$f")) / 86400 ))
            if (( mod_days <= 7 )); then
                printf "     %-30s Modified: %d days ago\n" "$f" "$mod_days"
                log_alert "INFO" "AUTH-FILE-MOD" "${f} was modified ${mod_days} day(s) ago" \
                    "Verify recent changes to ${f} are authorized."
            fi
        fi
    done

    # ---- 10d: SUID/SGID Binary Audit ----
    echo ""
    echo "  [10d] SUID/SGID Binary Audit"
    echo "  -----------------------------------------------"

    # Standard allowed SUID paths
    local allowed_suid_dirs="/usr/bin /usr/sbin /usr/lib /usr/lib64 /usr/libexec /bin /sbin"

    echo "     SUID files outside standard paths:"
    local suspicious_suid=0
    find / -perm -4000 -type f 2>/dev/null | while read f; do
        local in_allowed=0
        for d in $allowed_suid_dirs; do
            [[ "$f" == "$d"/* ]] && in_allowed=1 && break
        done
        if (( in_allowed == 0 )); then
            local owner=$(stat -c '%U:%G' "$f" 2>/dev/null)
            local perms=$(stat -c '%a' "$f" 2>/dev/null)
            printf "     *** SUSPICIOUS: %-40s Owner:%-15s Perms:%s\n" "$f" "$owner" "$perms"
            log_alert "CRITICAL" "SUID-SUSPICIOUS" "Suspicious SUID binary: ${f} (owner: ${owner})" \
                "VAPT Critical: Investigate SUID binary ${f}. Remove SUID if not required: chmod u-s ${f}"
            suspicious_suid=1
        fi
    done
    (( suspicious_suid == 0 )) && echo "     No suspicious SUID files found outside standard paths."

    echo ""
    echo "     SGID files outside standard paths:"
    local suspicious_sgid=0
    find / -perm -2000 -type f 2>/dev/null | while read f; do
        local in_allowed=0
        for d in $allowed_suid_dirs; do
            [[ "$f" == "$d"/* ]] && in_allowed=1 && break
        done
        if (( in_allowed == 0 )); then
            printf "     *** SUSPICIOUS SGID: %s\n" "$f"
            suspicious_sgid=1
        fi
    done
    (( suspicious_sgid == 0 )) && echo "     No suspicious SGID files found."

    # ---- 10e: World-Writable Files & Dirs ----
    echo ""
    echo "  [10e] World-Writable Files & Directories"
    echo "  -----------------------------------------------"

    echo "     World-writable directories without sticky bit:"
    find / -path /proc -prune -o -path /sys -prune -o -path /dev -prune -o \
        -type d -perm -0002 ! -perm -1000 -print 2>/dev/null | head -20 | while read d; do
        printf "     *** %s ***\n" "$d"
        log_alert "WARNING" "WORLD-WRITABLE" "World-writable dir without sticky bit: ${d}" \
            "Set sticky bit: chmod +t ${d} (or restrict permissions)"
    done

    echo ""
    echo "     World-writable files in critical paths:"
    for dir in /etc /usr /var /opt; do
        find "$dir" -type f -perm -0002 2>/dev/null | head -5 | while read f; do
            printf "     *** %s ***\n" "$f"
            log_alert "CRITICAL" "WORLD-WRITABLE-FILE" "World-writable file in critical path: ${f}" \
                "VAPT Finding: Remove world-write: chmod o-w ${f}"
        done
    done

    # ---- 10f: Firewall & SELinux Status ----
    echo ""
    echo "  [10f] Firewall & SELinux Status"
    echo "  -----------------------------------------------"

    # Firewall
    echo "     Firewall Status:"
    if check_command firewall-cmd; then
        local fw_state
        fw_state=$(firewall-cmd --state 2>/dev/null)
        printf "     firewalld: %s\n" "$fw_state"
        if [[ "$fw_state" != "running" ]]; then
            log_alert "CRITICAL" "FIREWALL" "firewalld is NOT running" \
                "VAPT Critical: Enable firewalld: systemctl enable --now firewalld"
        fi
        echo "     Active zones:"
        firewall-cmd --get-active-zones 2>/dev/null | sed 's/^/       /'
    elif check_command iptables; then
        local rule_count
        rule_count=$(iptables -L -n 2>/dev/null | grep -c "^[A-Z]")
        printf "     iptables rules: %d\n" "$rule_count"
        if (( rule_count < 3 )); then
            log_alert "WARNING" "FIREWALL" "iptables has very few rules (${rule_count})" \
                "Review firewall configuration. Minimal rules may indicate no protection."
        fi
    else
        echo "     No firewall tool detected."
        log_alert "CRITICAL" "FIREWALL" "No firewall (firewalld/iptables) detected" \
            "VAPT Critical: Install and configure firewall immediately."
    fi

    # SELinux
    echo ""
    echo "     SELinux Status:"
    local selinux_mode
    selinux_mode=$(getenforce 2>/dev/null || echo "Not Installed")
    printf "     Mode: %s\n" "$selinux_mode"
    if [[ "$selinux_mode" == "Disabled" || "$selinux_mode" == "Permissive" ]]; then
        log_alert "CRITICAL" "SELINUX" "SELinux is ${selinux_mode} (should be Enforcing)" \
            "VAPT Critical: Set SELinux to Enforcing: setenforce 1 && sed -i 's/SELINUX=.*/SELINUX=enforcing/' /etc/selinux/config"
    fi

    # SELinux denials in last 24h
    if [[ -f /var/log/audit/audit.log ]]; then
        local denial_count
        denial_count=$(grep "type=AVC.*denied" /var/log/audit/audit.log 2>/dev/null | \
            awk -v cutoff=$(date -d "24 hours ago" +%s) '{
                match($0, /msg=audit\(([0-9]+)/, a);
                if(a[1]+0 >= cutoff) count++
            } END{print count+0}')
        printf "     SELinux Denials (24h): %s\n" "$denial_count"
        if (( denial_count > 50 )); then
            log_alert "WARNING" "SELINUX-DENIALS" "${denial_count} SELinux denials in last 24h" \
                "High SELinux denial count. Review with: ausearch -m AVC -ts recent | audit2why"
        fi
    fi

    # ---- 10g: Open Ports & Listening Services ----
    echo ""
    echo "  [10g] Open Ports & Listening Services"
    echo "  -----------------------------------------------"
    if check_command ss; then
        printf "     %-8s %-25s %-25s %s\n" "PROTO" "LOCAL ADDRESS" "STATE" "PROCESS"
        echo "     -----------------------------------------------------------------------"
        ss -tulnp 2>/dev/null | grep LISTEN | while read proto recvq sendq local_addr peer_addr state process; do
            # Some ss versions have different column orders
            if [[ "$state" == "LISTEN" ]] || [[ "$local_addr" == *":"* ]]; then
                local proc_name
                proc_name=$(echo "$process" | grep -oP 'users:\(\("\K[^"]+' || echo "$process")
                printf "     %-8s %-25s %-25s %s\n" "$proto" "$local_addr" "LISTEN" "${proc_name:0:30}"
            fi
        done
    elif check_command netstat; then
        netstat -tulnp 2>/dev/null | grep LISTEN | head -30 | sed 's/^/     /'
    fi

    # Count listening ports
    local port_count
    port_count=$(ss -tulnp 2>/dev/null | grep -c LISTEN || echo 0)
    echo ""
    printf "     Total listening ports: %d\n" "$port_count"

    # ---- 10h: Certificate Expiry Check ----
    echo ""
    echo "  [10h] SSL/TLS Certificate Expiry Check"
    echo "  -----------------------------------------------"

    if check_command openssl; then
        local cert_found=0
        for cert_dir in $CERT_SCAN_PATHS; do
            [[ ! -d "$cert_dir" ]] && continue
            find "$cert_dir" -type f \( -name "*.pem" -o -name "*.crt" -o -name "*.cert" \) 2>/dev/null | while read cert; do
                local expiry_date expiry_epoch days_left
                expiry_date=$(openssl x509 -enddate -noout -in "$cert" 2>/dev/null | cut -d= -f2)
                [[ -z "$expiry_date" ]] && continue
                expiry_epoch=$(date -d "$expiry_date" +%s 2>/dev/null)
                [[ -z "$expiry_epoch" ]] && continue
                days_left=$(( (expiry_epoch - $(date +%s)) / 86400 ))
                cert_found=1

                local status="OK"
                if (( days_left < 0 )); then
                    status="EXPIRED"
                    log_alert "CRITICAL" "CERT-EXPIRED" "Certificate EXPIRED: ${cert} (expired ${days_left#-} days ago)" \
                        "IMMEDIATE: Renew expired certificate ${cert}. Notify Application/Security team."
                elif (( days_left <= THRESH_CERT_CRIT_DAYS )); then
                    status="CRITICAL"
                    log_alert "CRITICAL" "CERT-EXPIRING" "Certificate expiring in ${days_left} days: ${cert}" \
                        "Renew certificate within ${days_left} days. Notify Application team."
                elif (( days_left <= THRESH_CERT_WARN_DAYS )); then
                    status="WARNING"
                    log_alert "WARNING" "CERT-EXPIRING" "Certificate expiring in ${days_left} days: ${cert}" \
                        "Plan certificate renewal for ${cert}."
                fi

                if [[ "$status" != "OK" ]]; then
                    local cn
                    cn=$(openssl x509 -subject -noout -in "$cert" 2>/dev/null | sed 's/.*CN\s*=\s*//')
                    printf "     %-50s CN:%-20s Days:%d [%s]\n" "${cert:0:50}" "${cn:0:20}" "$days_left" "$status"
                fi
            done
        done
        (( cert_found == 0 )) && echo "     No certificates found in scan paths."
    else
        echo "     [SKIPPED] openssl not available"
    fi

    # ---- 10i: Password Policy Compliance ----
    echo ""
    echo "  [10i] Password Policy Compliance"
    echo "  -----------------------------------------------"

    echo "     /etc/login.defs Check:"
    if [[ -f /etc/login.defs ]]; then
        declare -A LOGIN_DEFS_CHECKS=(
            ["PASS_MAX_DAYS"]="$CIS_PASS_MAX_DAYS"
            ["PASS_MIN_DAYS"]="$CIS_PASS_MIN_DAYS"
            ["PASS_MIN_LEN"]="$CIS_PASS_MIN_LEN"
            ["PASS_WARN_AGE"]="$CIS_PASS_WARN_AGE"
        )

        printf "     %-20s %-10s %-10s %-10s\n" "PARAMETER" "CURRENT" "CIS REQ" "STATUS"
        echo "     -----------------------------------------------------------"
        for param in PASS_MAX_DAYS PASS_MIN_DAYS PASS_MIN_LEN PASS_WARN_AGE; do
            local current expected status
            current=$(grep "^${param}" /etc/login.defs 2>/dev/null | awk '{print $2}')
            expected="${LOGIN_DEFS_CHECKS[$param]}"
            [[ -z "$current" ]] && current="(not set)"

            if [[ "$current" == "(not set)" ]]; then
                status="MISSING"
            elif [[ "$param" == "PASS_MAX_DAYS" ]] && (( current > expected )); then
                status="NON-COMPLIANT"
            elif [[ "$param" == "PASS_MIN_DAYS" ]] && (( current < expected )); then
                status="NON-COMPLIANT"
            elif [[ "$param" == "PASS_MIN_LEN" ]] && (( current < expected )); then
                status="NON-COMPLIANT"
            elif [[ "$param" == "PASS_WARN_AGE" ]] && (( current < expected )); then
                status="NON-COMPLIANT"
            else
                status="COMPLIANT"
            fi

            printf "     %-20s %-10s %-10s %-10s\n" "$param" "$current" "$expected" "$status"
            if [[ "$status" == "NON-COMPLIANT" || "$status" == "MISSING" ]]; then
                log_alert "WARNING" "PASSWORD-POLICY" "${param}=${current} (CIS requires ${expected})" \
                    "Update /etc/login.defs: Set ${param} to ${expected}"
            fi
        done
    fi

    # PAM password quality
    echo ""
    echo "     PAM Password Quality (pwquality):"
    local pwquality_conf="/etc/security/pwquality.conf"
    if [[ -f "$pwquality_conf" ]]; then
        for param in minlen minclass dcredit ucredit lcredit ocredit; do
            local val
            val=$(grep "^${param}" "$pwquality_conf" 2>/dev/null | awk -F= '{print $2}' | tr -d ' ')
            [[ -n "$val" ]] && printf "     %-20s = %s\n" "$param" "$val"
        done
    else
        echo "     /etc/security/pwquality.conf not found"
        log_alert "WARNING" "PWQUALITY" "pwquality.conf not configured" \
            "Configure password complexity in /etc/security/pwquality.conf per CIS benchmarks."
    fi

    # ---- 10j: Audit Daemon Health ----
    echo ""
    echo "  [10j] Audit Daemon (auditd) Health"
    echo "  -----------------------------------------------"

    local auditd_status
    auditd_status=$(systemctl is-active auditd 2>/dev/null || echo "inactive")
    local auditd_enabled
    auditd_enabled=$(systemctl is-enabled auditd 2>/dev/null || echo "disabled")

    printf "     auditd Service  : %s (enabled: %s)\n" "$auditd_status" "$auditd_enabled"

    if [[ "$auditd_status" != "active" ]]; then
        log_alert "CRITICAL" "AUDITD" "auditd is NOT running (status: ${auditd_status})" \
            "VAPT Critical: Start auditd: systemctl start auditd && systemctl enable auditd"
    fi

    if check_command auditctl; then
        local rule_count
        rule_count=$(auditctl -l 2>/dev/null | grep -cv "^No rules" || echo 0)
        printf "     Audit Rules     : %d loaded\n" "$rule_count"
        if (( rule_count < 5 )); then
            log_alert "WARNING" "AUDIT-RULES" "Only ${rule_count} audit rules loaded (expected more)" \
                "Review audit rules. Add rules for: file integrity, privilege escalation, user management."
        fi

        # Check audit log size
        if [[ -f /var/log/audit/audit.log ]]; then
            local audit_size_mb
            audit_size_mb=$(du -sm /var/log/audit/audit.log 2>/dev/null | awk '{print $1}')
            printf "     Audit Log Size  : %s MB\n" "$audit_size_mb"
        fi

        # Backlog check
        local backlog
        backlog=$(auditctl -s 2>/dev/null | grep "backlog " | awk '{print $2}')
        local backlog_limit
        backlog_limit=$(auditctl -s 2>/dev/null | grep "backlog_limit" | awk '{print $2}')
        if [[ -n "$backlog" && -n "$backlog_limit" ]]; then
            printf "     Backlog         : %s / %s\n" "$backlog" "$backlog_limit"
            if (( backlog > backlog_limit * 80 / 100 )); then
                log_alert "WARNING" "AUDIT-BACKLOG" "Audit backlog ${backlog}/${backlog_limit} (>80%)" \
                    "Audit backlog nearing limit. Increase backlog_limit or investigate event flood."
            fi
        fi
    else
        echo "     auditctl not available (install audit package)"
    fi
    echo ""
}

#=============================================================================
# SECTION 11: CIS BENCHMARK COMPLIANCE
#=============================================================================
collect_cis_compliance() {
    section_header "11" "CIS BENCHMARK COMPLIANCE CHECKS"
    echo "================================================================================"

    # ---- 11a: Kernel Security Parameters ----
    echo ""
    echo "  [11a] Kernel Security Parameters (sysctl)"
    echo "  -----------------------------------------------"
    printf "     %-50s %-10s %-10s %-10s\n" "PARAMETER" "CURRENT" "EXPECTED" "STATUS"
    echo "     ---------------------------------------------------------------------------"

    for param in "${!CIS_SYSCTL[@]}"; do
        local expected="${CIS_SYSCTL[$param]}"
        local current
        current=$(sysctl -n "$param" 2>/dev/null || echo "N/A")

        local status="COMPLIANT"
        if [[ "$current" == "N/A" ]]; then
            status="N/A"
        elif [[ "$current" != "$expected" ]]; then
            status="NON-COMPLIANT"
            log_alert "WARNING" "CIS-SYSCTL" "Kernel param ${param}=${current} (expected: ${expected})" \
                "Fix: sysctl -w ${param}=${expected} and add to /etc/sysctl.d/99-cis.conf"
        fi
        printf "     %-50s %-10s %-10s %-10s\n" "$param" "$current" "$expected" "$status"
    done

    # ---- 11b: Disabled Kernel Modules ----
    echo ""
    echo "  [11b] Disabled Kernel Modules (CIS)"
    echo "  -----------------------------------------------"
    for mod in $CIS_DISABLED_MODULES; do
        local mod_status="LOADED"
        if lsmod 2>/dev/null | grep -q "^${mod}"; then
            mod_status="LOADED *** NON-COMPLIANT ***"
            log_alert "WARNING" "CIS-MODULE" "Kernel module '${mod}' is loaded (should be disabled)" \
                "Disable: echo 'install ${mod} /bin/true' > /etc/modprobe.d/${mod}.conf && rmmod ${mod}"
        elif [[ -f "/etc/modprobe.d/${mod}.conf" ]] || grep -rq "install ${mod} /bin/true" /etc/modprobe.d/ 2>/dev/null; then
            mod_status="DISABLED (compliant)"
        else
            mod_status="NOT LOADED (no explicit disable rule)"
        fi
        printf "     %-20s : %s\n" "$mod" "$mod_status"
    done

    # ---- 11c: Filesystem Security ----
    echo ""
    echo "  [11c] Filesystem Security Checks"
    echo "  -----------------------------------------------"

    # /tmp partition
    echo "     Separate /tmp partition:"
    if mount | grep -q "on /tmp "; then
        local tmp_opts=$(mount | grep "on /tmp " | awk '{print $6}')
        echo "     /tmp is separate partition. Options: ${tmp_opts}"
        for opt in noexec nosuid nodev; do
            if ! echo "$tmp_opts" | grep -q "$opt"; then
                echo "     *** MISSING: ${opt} on /tmp ***"
                log_alert "WARNING" "CIS-TMP" "/tmp missing mount option: ${opt}" \
                    "Add ${opt} to /tmp mount options in /etc/fstab"
            fi
        done
    else
        echo "     *** /tmp is NOT a separate partition ***"
        log_alert "WARNING" "CIS-TMP" "/tmp is not a separate partition" \
            "CIS: Create separate /tmp partition with noexec,nosuid,nodev options."
    fi

    # /var/tmp
    echo ""
    echo "     /var/tmp bind mount:"
    if mount | grep -q "on /var/tmp "; then
        echo "     /var/tmp is mounted separately (compliant)"
    else
        echo "     /var/tmp not separately mounted (review needed)"
    fi

    # /dev/shm
    echo ""
    echo "     /dev/shm security:"
    local shm_opts=$(mount | grep "on /dev/shm " | awk '{print $6}')
    if [[ -n "$shm_opts" ]]; then
        echo "     /dev/shm options: ${shm_opts}"
        if ! echo "$shm_opts" | grep -q "noexec"; then
            log_alert "WARNING" "CIS-SHM" "/dev/shm missing noexec option" \
                "Add noexec,nosuid,nodev to /dev/shm in /etc/fstab"
        fi
    fi

    # Core dumps
    echo ""
    echo "     Core Dumps:"
    local core_soft=$(ulimit -Sc 2>/dev/null)
    local core_hard=$(ulimit -Hc 2>/dev/null)
    local core_sysctl=$(sysctl -n fs.suid_dumpable 2>/dev/null)
    printf "     Soft limit: %s | Hard limit: %s | suid_dumpable: %s\n" "$core_soft" "$core_hard" "$core_sysctl"
    if [[ "$core_soft" != "0" ]] || [[ "$core_sysctl" != "0" ]]; then
        log_alert "WARNING" "CIS-COREDUMP" "Core dumps not fully disabled (soft:${core_soft}, suid_dumpable:${core_sysctl})" \
            "Disable core dumps: add '* hard core 0' to /etc/security/limits.conf and set fs.suid_dumpable=0"
    fi

    # ASLR
    echo ""
    echo "     ASLR (Address Space Layout Randomization):"
    local aslr=$(sysctl -n kernel.randomize_va_space 2>/dev/null)
    printf "     kernel.randomize_va_space = %s (expected: 2)\n" "$aslr"
    if [[ "$aslr" != "2" ]]; then
        log_alert "CRITICAL" "CIS-ASLR" "ASLR not fully enabled (value: ${aslr}, expected: 2)" \
            "Enable ASLR: sysctl -w kernel.randomize_va_space=2"
    fi

    # ---- 11d: File Integrity Monitoring ----
    echo ""
    echo "  [11d] File Integrity Monitoring (AIDE/OSSEC)"
    echo "  -----------------------------------------------"

    local fim_found=0
    if check_command aide; then
        echo "     AIDE: Installed"
        local aide_db="/var/lib/aide/aide.db.gz"
        [[ -f "$aide_db" ]] && echo "     AIDE DB: ${aide_db} (exists)" || echo "     AIDE DB: NOT initialized"

        # Last AIDE check
        local aide_log="/var/log/aide/aide.log"
        if [[ -f "$aide_log" ]]; then
            local last_check
            last_check=$(stat -c '%y' "$aide_log" 2>/dev/null | cut -d. -f1)
            echo "     Last Check: ${last_check}"
        fi
        fim_found=1
    else
        echo "     AIDE: NOT installed"
    fi

    if check_command ossec-control || [[ -d "/var/ossec" ]]; then
        echo "     OSSEC: Installed"
        fim_found=1
    fi

    if (( fim_found == 0 )); then
        echo "     *** No File Integrity Monitoring tool detected ***"
        log_alert "CRITICAL" "CIS-FIM" "No File Integrity Monitoring (AIDE/OSSEC) installed" \
            "VAPT Critical: Install AIDE: yum install aide && aide --init && aide --check"
    fi

    # ---- 11e: Umask Validation ----
    echo ""
    echo "  [11e] System Umask Validation"
    echo "  -----------------------------------------------"

    for f in /etc/profile /etc/bashrc /etc/login.defs; do
        if [[ -f "$f" ]]; then
            local umask_val
            umask_val=$(grep -i "^umask\|^[[:space:]]*umask" "$f" 2>/dev/null | tail -1 | awk '{print $2}')
            if [[ -n "$umask_val" ]]; then
                local status="COMPLIANT"
                # Check if umask is restrictive enough (027 or 077)
                if [[ "$umask_val" != "027" && "$umask_val" != "077" && "$umask_val" != "0027" && "$umask_val" != "0077" ]]; then
                    status="NON-COMPLIANT"
                    log_alert "WARNING" "CIS-UMASK" "Umask in ${f} is ${umask_val} (expected: ${CIS_UMASK} or stricter)" \
                        "Set umask to ${CIS_UMASK} in ${f}"
                fi
                printf "     %-25s umask: %-8s [%s]\n" "$f" "$umask_val" "$status"
            else
                printf "     %-25s umask: (not set)\n" "$f"
            fi
        fi
    done
    echo ""
}

#=============================================================================
# SECTION 12: INFRASTRUCTURE & OPERATIONAL CHECKS
# (Excludes: Time sync, Bonding/Teaming, Multipath - vSphere managed)
#=============================================================================
collect_infra_checks() {
    section_header "12" "INFRASTRUCTURE & OPERATIONAL CHECKS"
    echo "================================================================================"

    # ---- 12a: Zombie & Defunct Processes ----
    echo ""
    echo "  [12a] Zombie & Defunct Processes"
    echo "  -----------------------------------------------"
    local zombie_count
    zombie_count=$(ps aux | awk '$8~/^Z/' | wc -l)
    printf "     Zombie process count: %d (threshold: %d)\n" "$zombie_count" "$THRESH_ZOMBIE"

    if (( zombie_count > 0 )); then
        echo "     Zombie processes:"
        printf "     %-8s %-8s %-10s %s\n" "PID" "PPID" "USER" "COMMAND"
        ps aux | awk '$8~/^Z/' | while read user pid cpu mem vsz rss tty stat start time cmd; do
            local ppid
            ppid=$(ps -o ppid= -p "$pid" 2>/dev/null | tr -d ' ')
            printf "     %-8s %-8s %-10s %s\n" "$pid" "$ppid" "$user" "${cmd:0:50}"
        done
        if (( zombie_count > THRESH_ZOMBIE )); then
            log_alert "WARNING" "ZOMBIE" "${zombie_count} zombie processes detected (threshold: ${THRESH_ZOMBIE})" \
                "Investigate parent processes. Notify Application team if app-related."
        fi
    fi

    # ---- 12b: Kernel & Pending Reboot ----
    echo ""
    echo "  [12b] Kernel & Pending Reboot Check"
    echo "  -----------------------------------------------"
    local running_kernel=$(uname -r)
    local installed_kernel
    installed_kernel=$(rpm -q kernel --last 2>/dev/null | head -1 | awk '{print $1}' | sed 's/kernel-//')

    printf "     Running Kernel  : %s\n" "$running_kernel"
    printf "     Latest Installed: %s\n" "${installed_kernel:-N/A}"

    if [[ -n "$installed_kernel" && "$running_kernel" != "$installed_kernel" ]]; then
        echo "     *** REBOOT PENDING - Kernel mismatch ***"
        log_alert "WARNING" "REBOOT-PENDING" "Running kernel (${running_kernel}) differs from installed (${installed_kernel})" \
            "System reboot required for new kernel. Schedule maintenance window."
    else
        echo "     Kernel is current (no reboot pending)."
    fi

    # needs-restarting check
    if check_command needs-restarting; then
        local restart_needed
        restart_needed=$(needs-restarting -r 2>/dev/null; echo $?)
        if [[ "$restart_needed" == "1" ]]; then
            echo "     needs-restarting: REBOOT RECOMMENDED"
            log_alert "WARNING" "NEEDS-RESTART" "System requires reboot (needs-restarting detected)" \
                "Schedule reboot during maintenance window."
        fi

        echo ""
        echo "     Services needing restart:"
        needs-restarting -s 2>/dev/null | head -10 | sed 's/^/     /'
    fi

    # ---- 12c: Package Security Errata ----
    echo ""
    echo "  [12c] Available Security Updates"
    echo "  -----------------------------------------------"

    if check_command yum; then
        local sec_updates
        sec_updates=$(yum updateinfo list security 2>/dev/null | grep -c "/Sec\." || echo "N/A")
        printf "     Pending security updates: %s\n" "$sec_updates"
        if [[ "$sec_updates" != "N/A" ]] && (( sec_updates > 0 )); then
            log_alert "WARNING" "SECURITY-PATCHES" "${sec_updates} security updates available" \
                "Apply security patches: yum update --security. Coordinate with Change Management."
            echo "     Critical/Important (top 10):"
            yum updateinfo list security 2>/dev/null | grep -iE "Critical|Important" | head -10 | sed 's/^/       /'
        fi
    elif check_command dnf; then
        local sec_updates
        sec_updates=$(dnf updateinfo list --security 2>/dev/null | grep -c "Sec\." || echo "N/A")
        printf "     Pending security updates: %s\n" "$sec_updates"
        if [[ "$sec_updates" != "N/A" ]] && (( sec_updates > 0 )); then
            log_alert "WARNING" "SECURITY-PATCHES" "${sec_updates} security updates available" \
                "Apply security patches: dnf update --security"
        fi
    fi

    # ---- 12d: DNS Resolution Health ----
    echo ""
    echo "  [12d] DNS Resolution Health"
    echo "  -----------------------------------------------"

    echo "     Nameservers configured:"
    grep "^nameserver" /etc/resolv.conf 2>/dev/null | while read _ ns; do
        # Test each nameserver
        local dns_result
        dns_result=$(timeout 5 nslookup "$(hostname)" "$ns" 2>/dev/null)
        if [[ $? -eq 0 ]]; then
            printf "     %-20s : RESPONSIVE\n" "$ns"
        else
            printf "     %-20s : *** FAILED ***\n" "$ns"
            log_alert "WARNING" "DNS" "Nameserver ${ns} not responding" \
                "DNS resolution failure for ${ns}. Update /etc/resolv.conf or notify Network team."
        fi
    done

    # ---- 12e: Backup Agent Check ----
    echo ""
    echo "  [12e] Backup Agent Status"
    echo "  -----------------------------------------------"

    local backup_found=0
    local backup_procs
    backup_procs=$(ps aux | grep -E "${BACKUP_AGENTS}" | grep -v grep)
    if [[ -n "$backup_procs" ]]; then
        echo "$backup_procs" | while read user pid cpu mem vsz rss tty stat start time cmd; do
            printf "     %-10s PID:%-8s %s\n" "$user" "$pid" "${cmd:0:60}"
        done
        backup_found=1
    fi

    if (( backup_found == 0 )); then
        echo "     *** No backup agent processes detected ***"
        log_alert "WARNING" "BACKUP-AGENT" "No backup agent processes running" \
            "Verify backup agent is installed and running. Notify Backup team."
    fi

    # ---- 12f: Failed Systemd Units ----
    echo ""
    echo "  [12f] Failed Systemd Units"
    echo "  -----------------------------------------------"
    local failed_units
    failed_units=$(systemctl --failed --no-legend 2>/dev/null)
    if [[ -n "$failed_units" ]]; then
        echo "$failed_units" | while read unit load active sub desc; do
            printf "     *** FAILED: %-40s (%s)\n" "$unit" "$sub"
            log_alert "WARNING" "SYSTEMD-FAILED" "Systemd unit '${unit}' is in failed state" \
                "Investigate: journalctl -u ${unit}. Restart if applicable."
        done
    else
        echo "     No failed systemd units."
    fi

    # ---- 12g: Kdump Status ----
    echo ""
    echo "  [12g] Kdump (Crash Dump) Status"
    echo "  -----------------------------------------------"
    local kdump_status
    kdump_status=$(systemctl is-active kdump 2>/dev/null || echo "inactive")
    local kdump_enabled
    kdump_enabled=$(systemctl is-enabled kdump 2>/dev/null || echo "disabled")
    printf "     kdump: %s (enabled: %s)\n" "$kdump_status" "$kdump_enabled"

    if [[ "$kdump_status" != "active" ]]; then
        log_alert "WARNING" "KDUMP" "kdump is not active (status: ${kdump_status})" \
            "Enable kdump for crash analysis: systemctl enable --now kdump"
    fi

    # Check crash directory
    local crash_dir="/var/crash"
    if [[ -d "$crash_dir" ]]; then
        local crash_count
        crash_count=$(find "$crash_dir" -mindepth 1 -maxdepth 1 -type d 2>/dev/null | wc -l)
        if (( crash_count > 0 )); then
            echo "     *** ${crash_count} crash dump(s) found in ${crash_dir} ***"
            find "$crash_dir" -mindepth 1 -maxdepth 1 -type d 2>/dev/null | while read d; do
                printf "     Crash: %s (%s)\n" "$(basename "$d")" "$(stat -c '%y' "$d" 2>/dev/null | cut -d. -f1)"
            done
            log_alert "WARNING" "CRASH-DUMPS" "${crash_count} crash dump(s) found in ${crash_dir}" \
                "Analyze crash dumps for root cause. Engage vendor/application team."
        fi
    fi

    # ---- 12h: Core Dump Detection ----
    echo ""
    echo "  [12h] Recent Core Dumps"
    echo "  -----------------------------------------------"
    if check_command coredumpctl; then
        local recent_cores
        recent_cores=$(coredumpctl list --since "24 hours ago" 2>/dev/null | grep -c "^\|present" || echo 0)
        if (( recent_cores > 0 )); then
            echo "     *** ${recent_cores} core dump(s) in last 24 hours ***"
            coredumpctl list --since "24 hours ago" 2>/dev/null | tail -10 | sed 's/^/     /'
            log_alert "WARNING" "CORE-DUMPS" "${recent_cores} core dumps detected in last 24h" \
                "Application crashes detected. Notify Application team for analysis."
        else
            echo "     No core dumps in last 24 hours."
        fi
    else
        # Fallback: check traditional locations
        local core_files
        core_files=$(find /var/crash /var/lib/systemd/coredump /tmp -name "core.*" -o -name "core" -mtime -1 2>/dev/null | wc -l)
        printf "     Core files found (last 24h): %d\n" "$core_files"
    fi

    # ---- 12i: Log Rotation & Oversized Logs ----
    echo ""
    echo "  [12i] Log Rotation & Oversized Logs"
    echo "  -----------------------------------------------"

    # Logrotate last run
    local logrotate_status
    if [[ -f /var/lib/logrotate/logrotate.status ]]; then
        local last_rotate
        last_rotate=$(stat -c '%y' /var/lib/logrotate/logrotate.status 2>/dev/null | cut -d. -f1)
        echo "     Last logrotate: ${last_rotate}"
    else
        echo "     logrotate status file not found"
        log_alert "WARNING" "LOGROTATE" "logrotate status file missing" \
            "Verify logrotate is configured and running."
    fi

    echo ""
    echo "     Oversized log files (>${THRESH_LOG_SIZE_MB}MB):"
    local oversized=0
    find /var/log -type f -size +${THRESH_LOG_SIZE_MB}M 2>/dev/null | while read f; do
        local size_mb
        size_mb=$(du -sm "$f" 2>/dev/null | awk '{print $1}')
        printf "     *** %-50s %s MB\n" "$f" "$size_mb"
        log_alert "WARNING" "OVERSIZED-LOG" "Log file ${f} is ${size_mb}MB (>${THRESH_LOG_SIZE_MB}MB)" \
            "Check logrotate config for ${f}. May need manual cleanup or rotation."
        oversized=1
    done
