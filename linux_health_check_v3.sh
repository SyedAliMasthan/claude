#=============================================================================
# SECTION 13: PATCHING & COMPLIANCE STATUS
#=============================================================================
collect_patching_status() {
    section_header "13" "PATCHING & COMPLIANCE STATUS"
    echo "================================================================================"

    # Last patching date
    echo ""
    echo "  [13a] Last Patching Activity"
    echo "  -----------------------------------------------"
    if check_command yum; then
        local last_patch_date
        last_patch_date=$(yum history list 2>/dev/null | awk 'NR==4{print $4,$5,$6}')
        echo "     Last yum transaction: ${last_patch_date:-N/A}"

        # Calculate days since last patch
        if [[ -n "$last_patch_date" ]]; then
            local patch_epoch
            patch_epoch=$(date -d "$last_patch_date" +%s 2>/dev/null)
            if [[ -n "$patch_epoch" ]]; then
                local days_since=$(( ($(date +%s) - patch_epoch) / 86400 ))
                printf "     Days since last patch: %d (threshold: %d)\n" "$days_since" "$THRESH_PATCH_DAYS"
                if (( days_since > THRESH_PATCH_DAYS )); then
                    log_alert "WARNING" "PATCHING" "Last patch was ${days_since} days ago (threshold: ${THRESH_PATCH_DAYS})" \
                        "System is overdue for patching. Schedule patching via Red Hat Satellite."
                fi
            fi
        fi

        echo ""
        echo "     Recent yum history (last 5):"
        yum history list 2>/dev/null | head -8 | sed 's/^/     /'
    fi

    # Red Hat Satellite registration
    echo ""
    echo "  [13b] Satellite/Subscription Status"
    echo "  -----------------------------------------------"
    if check_command subscription-manager; then
        local sub_status
        sub_status=$(subscription-manager status 2>/dev/null | grep "Overall Status" | awk -F: '{print $2}' | tr -d ' ')
        printf "     Subscription Status: %s\n" "${sub_status:-N/A}"
        if [[ "$sub_status" != "Current" && -n "$sub_status" ]]; then
            log_alert "WARNING" "SUBSCRIPTION" "System subscription status: ${sub_status}" \
                "System may not be receiving updates. Check subscription-manager."
        fi
    else
        echo "     subscription-manager not available"
    fi

    # Compliance summary
    echo ""
    echo "  [13c] Compliance Summary"
    echo "  -----------------------------------------------"

    local total_findings critical_findings warning_findings
    total_findings=$(wc -l < "${ALERT_LOG}" 2>/dev/null || echo 0)
    critical_findings=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    warning_findings=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)

    echo "     ========================================="
    echo "     TOTAL FINDINGS    : ${total_findings}"
    echo "     CRITICAL          : ${critical_findings}"
    echo "     WARNING           : ${warning_findings}"
    echo "     ========================================="

    if (( critical_findings > 0 )); then
        echo "     STATUS: *** NON-COMPLIANT - CRITICAL FINDINGS ***"
    elif (( warning_findings > 0 )); then
        echo "     STATUS: PARTIALLY COMPLIANT - WARNINGS PRESENT"
    else
        echo "     STATUS: COMPLIANT - ALL CHECKS PASSED"
    fi
    echo ""
}

