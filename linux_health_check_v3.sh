#=============================================================================
# EMAIL ALERTING
#=============================================================================
send_email_alert() {
    [[ "${ENABLE_EMAIL_ALERT}" != "yes" ]] && return
    [[ ! -s "${ALERT_LOG}" ]] && return
    if ! check_command mail && ! check_command mailx && ! check_command sendmail; then
        echo "WARNING: No mail command found. Email skipped." >&2
        return
    fi

    local cc wc
    cc=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    wc=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)

    local subject="${EMAIL_SUBJECT_PREFIX} ${HOSTNAME_S}: ${cc} CRITICAL, ${wc} WARNING - ${DATE_STAMP}"
    local body="Linux Health & Security Check Alert - ${HOSTNAME_S}\n"
    body+="Generated: ${TIME_STAMP}\nOS: ${OS_VERSION}\n"
    body+="============================================\n\n"

    while IFS='|' read -r sev comp msg rec; do
        body+="[${sev}] ${comp}: ${msg}\n  --> ${rec}\n\n"
    done < "${ALERT_LOG}"

    body+="\nFull reports: ${TXT_FILE} | ${HTML_FILE}\n"

    if check_command mailx; then
        echo -e "$body" | mailx -s "$subject" -a "${HTML_FILE}" "${EMAIL_RECIPIENTS}" 2>/dev/null
    elif check_command mail; then
        echo -e "$body" | mail -s "$subject" "${EMAIL_RECIPIENTS}" 2>/dev/null
    fi
}

#=============================================================================
# SALT EVENT PUBLISHING
#=============================================================================
send_salt_event() {
    [[ "${ENABLE_SALT_EVENT}" != "yes" ]] && return
    [[ ! -s "${ALERT_LOG}" ]] && return
    if ! check_command salt-call; then
        echo "WARNING: salt-call not found. Salt event skipped." >&2
        return
    fi

    local cc wc
    cc=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    wc=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)

    local alerts_json="["
    local first=1
    while IFS='|' read -r sev comp msg rec; do
        (( first )) || alerts_json+=","
        first=0
        msg=$(echo "$msg" | sed 's/"/\\"/g')
        rec=$(echo "$rec" | sed 's/"/\\"/g')
        alerts_json+="{\"severity\":\"${sev}\",\"component\":\"${comp}\",\"message\":\"${msg}\",\"recommendation\":\"${rec}\"}"
    done < "${ALERT_LOG}"
    alerts_json+="]"

    local payload="{\"hostname\":\"${HOSTNAME_S}\",\"timestamp\":\"${TIME_STAMP}\",\"critical\":${cc},\"warning\":${wc},\"alerts\":${alerts_json}}"

    salt-call event.send "${SALT_EVENT_TAG}" "${payload}" --local 2>/dev/null || \
    salt-call event.fire_master "${payload}" "${SALT_EVENT_TAG}" 2>/dev/null || \
    echo "WARNING: Failed to publish Salt event." >&2
}

