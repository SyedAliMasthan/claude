#=============================================================================
# SECTION 2: CPU STATISTICS
#=============================================================================
collect_cpu_stats() {
    section_header "2" "CPU STATISTICS"

    if check_command mpstat; then
        echo "  >> Current CPU Utilization (mpstat 1 3 average):"
        local mpstat_out
        mpstat_out=$(mpstat 1 3 2>/dev/null | tail -1)
        local idle=$(echo "$mpstat_out" | awk '{print $NF}')
        local usage
        usage=$(echo "100 - ${idle}" | bc 2>/dev/null || echo "N/A")
        echo "     Overall CPU Usage : ${usage}%"
        if [[ "$usage" != "N/A" ]] && (( $(echo "$usage > $THRESH_CPU" | bc -l 2>/dev/null || echo 0) )); then
            log_alert "CRITICAL" "CPU" "Current CPU usage at ${usage}% (threshold: ${THRESH_CPU}%)" \
                "Investigate top CPU consumers. Notify Application/Oracle team if DB processes dominate."
        fi
        echo ""
        mpstat -P ALL 1 1 2>/dev/null | head -20
    else
        echo "  [SKIPPED] mpstat not available (install sysstat)"
        top -bn1 | head -5
    fi

    echo ""
    echo "  >> Load Average:"
    local load_1 load_5 load_15
    read load_1 load_5 load_15 _ < /proc/loadavg
    local load_thresh=$((CPU_COUNT * THRESH_LOAD_FACTOR))
    printf "     1-min: %-8s  5-min: %-8s  15-min: %-8s  (threshold: %d)\n" \
           "$load_1" "$load_5" "$load_15" "$load_thresh"
    if (( $(echo "$load_1 > $load_thresh" | bc -l 2>/dev/null || echo 0) )); then
        log_alert "WARNING" "CPU-LOAD" "Load average ${load_1} exceeds threshold ${load_thresh}" \
            "High load detected. Check for runaway processes."
    fi

    echo ""
    echo "  >> Top 5 CPU Consuming Processes:"
    printf "     %-8s %-6s %-6s %-10s %s\n" "PID" "%CPU" "%MEM" "USER" "COMMAND"
    echo "     -------------------------------------------------------"
    ps aux --sort=-%cpu | head -6 | tail -5 | while read user pid cpu mem vsz rss tty stat start time cmd; do
        printf "     %-8s %-6s %-6s %-10s %s\n" "$pid" "$cpu" "$mem" "$user" "${cmd:0:50}"
    done

    echo ""
    echo "  >> Oracle Process CPU Check:"
    local ora_cpu
    ora_cpu=$(ps aux | grep -E "${ORACLE_PROCS}" | grep -v grep)
    if [[ -n "$ora_cpu" ]]; then
        echo "$ora_cpu" | sort -k3 -rn | head -5 | while read user pid cpu mem vsz rss tty stat start time cmd; do
            printf "     %-8s %-6s %-6s %-10s %s\n" "$pid" "$cpu" "$mem" "$user" "${cmd:0:50}"
            if (( $(echo "$cpu > $THRESH_CPU" | bc -l 2>/dev/null || echo 0) )); then
                log_alert "CRITICAL" "ORACLE-CPU" "Oracle process ${cmd:0:30} (PID:${pid}) at ${cpu}% CPU" \
                    "Notify Oracle DBA team. Check for long-running queries or rman jobs."
            fi
        done
    else
        echo "     No Oracle processes detected."
    fi
    echo ""
}

#=============================================================================
# SECTION 3: MEMORY STATISTICS
#=============================================================================
collect_memory_stats() {
    section_header "3" "MEMORY STATISTICS"

    local mem_total mem_used mem_free mem_available mem_buffers mem_cached mem_pct
    mem_total=$(grep MemTotal /proc/meminfo | awk '{print $2}')
    mem_free=$(grep MemFree /proc/meminfo | awk '{print $2}')
    mem_available=$(grep MemAvailable /proc/meminfo | awk '{print $2}')
    mem_buffers=$(grep Buffers /proc/meminfo | awk '{print $2}')
    mem_cached=$(grep "^Cached:" /proc/meminfo | awk '{print $2}')
    mem_used=$((mem_total - mem_free - mem_buffers - mem_cached))
    mem_pct=$((mem_used * 100 / mem_total))

    local total_gb=$(echo "scale=2; $mem_total/1048576" | bc 2>/dev/null || echo "N/A")
    local used_gb=$(echo "scale=2; $mem_used/1048576" | bc 2>/dev/null || echo "N/A")
    local free_gb=$(echo "scale=2; $mem_free/1048576" | bc 2>/dev/null || echo "N/A")
    local avail_gb=$(echo "scale=2; $mem_available/1048576" | bc 2>/dev/null || echo "N/A")
    local buf_gb=$(echo "scale=2; $mem_buffers/1048576" | bc 2>/dev/null || echo "N/A")
    local cache_gb=$(echo "scale=2; $mem_cached/1048576" | bc 2>/dev/null || echo "N/A")

    echo "  >> Physical Memory:"
    printf "     Total: %-10s  Used: %-10s  Free: %-10s\n" "${total_gb} GB" "${used_gb} GB" "${free_gb} GB"
    printf "     Available: %-10s  Buffers: %-10s  Cached: %-10s\n" "${avail_gb} GB" "${buf_gb} GB" "${cache_gb} GB"
    printf "     Utilization: %s\n" "$(pct_bar $mem_pct)"

    if (( mem_pct > THRESH_MEMORY )); then
        log_alert "CRITICAL" "MEMORY" "Memory utilization at ${mem_pct}% (threshold: ${THRESH_MEMORY}%)" \
            "Investigate high memory consumers. Notify Application team for memory leak / Oracle DBA for SGA/PGA tuning."
    fi

    echo ""
    echo "  >> Huge Pages (Oracle relevant):"
    grep -i huge /proc/meminfo 2>/dev/null | while read line; do echo "     $line"; done

    echo ""
    echo "  >> Top 5 Memory Consumers:"
    printf "     %-8s %-6s %-12s %-10s %s\n" "PID" "%MEM" "RSS(MB)" "USER" "COMMAND"
    echo "     -------------------------------------------------------"
    ps aux --sort=-%mem | head -6 | tail -5 | while read user pid cpu mem vsz rss tty stat start time cmd; do
        local rss_mb=$((rss / 1024))
        printf "     %-8s %-6s %-12s %-10s %s\n" "$pid" "$mem" "$rss_mb" "$user" "${cmd:0:50}"
    done
    echo ""
}

#=============================================================================
# SECTION 4: SWAP UTILIZATION
#=============================================================================
collect_swap_stats() {
    section_header "4" "SWAP UTILIZATION"

    local swap_total swap_used swap_free swap_pct
    swap_total=$(grep SwapTotal /proc/meminfo | awk '{print $2}')
    swap_used=$((swap_total - $(grep SwapFree /proc/meminfo | awk '{print $2}')))
    swap_free=$(grep SwapFree /proc/meminfo | awk '{print $2}')

    if (( swap_total > 0 )); then
        swap_pct=$((swap_used * 100 / swap_total))
    else
        swap_pct=0
    fi

    local stotal_gb=$(echo "scale=2; $swap_total/1048576" | bc 2>/dev/null || echo "N/A")
    local sused_gb=$(echo "scale=2; $swap_used/1048576" | bc 2>/dev/null || echo "N/A")
    local sfree_gb=$(echo "scale=2; $swap_free/1048576" | bc 2>/dev/null || echo "N/A")

    printf "     Total: %-10s  Used: %-10s  Free: %-10s\n" "${stotal_gb} GB" "${sused_gb} GB" "${sfree_gb} GB"
    printf "     Utilization: %s\n" "$(pct_bar $swap_pct)"

    if (( swap_pct > THRESH_SWAP )); then
        log_alert "CRITICAL" "SWAP" "Swap usage at ${swap_pct}% (threshold: ${THRESH_SWAP}%)" \
            "High swap = memory pressure. Notify Application/Oracle team."
    fi

    echo ""
    echo "  >> Top 10 Processes Consuming Swap:"
    printf "     %-8s %-12s %-10s %s\n" "PID" "SWAP(MB)" "USER" "COMMAND"
    echo "     -------------------------------------------------------"

    for pid_dir in /proc/[0-9]*; do
        local pid=$(basename "$pid_dir")
        local swap_kb=0
        if [[ -f "${pid_dir}/smaps_rollup" ]]; then
            swap_kb=$(awk '/^Swap:/{sum+=$2}END{print sum+0}' "${pid_dir}/smaps_rollup" 2>/dev/null)
        elif [[ -f "${pid_dir}/smaps" ]]; then
            swap_kb=$(awk '/^Swap:/{sum+=$2}END{print sum+0}' "${pid_dir}/smaps" 2>/dev/null)
        fi
        if [[ -n "$swap_kb" ]] && (( swap_kb > 1024 )); then
            local cmd_name=$(cat "${pid_dir}/comm" 2>/dev/null || echo "unknown")
            local proc_user=$(stat -c '%U' "${pid_dir}" 2>/dev/null || echo "unknown")
            echo "${swap_kb} ${pid} ${proc_user} ${cmd_name}"
        fi
    done 2>/dev/null | sort -rn | head -10 | while read swap_kb pid user cmd; do
        local swap_mb=$((swap_kb / 1024))
        printf "     %-8s %-12s %-10s %s\n" "$pid" "$swap_mb" "$user" "${cmd:0:50}"
    done
    echo ""
}

#=============================================================================
# SECTION 5: DISK I/O & LATENCY
#=============================================================================
collect_disk_io() {
    section_header "5" "DISK I/O & LATENCY"

    if check_command iostat; then
        echo "  >> Current I/O Statistics (iostat -xd 1 2):"
        printf "     %-12s %8s %8s %8s %8s %8s %8s %8s\n" \
               "DEVICE" "rrqm/s" "wrqm/s" "r/s" "w/s" "await" "svctm" "%util"
        echo "     ---------------------------------------------------------------------------"

        iostat -xd 1 2 2>/dev/null | awk '
        NR>1 && /^[a-z]/ && seen[$1]++ {
            printf "     %-12s %8s %8s %8s %8s %8s %8s %8s\n",
                   $1, $2, $3, $4, $5, $(NF-2), $(NF-1), $NF
        }' | while read line; do
            echo "$line"
            local dev=$(echo "$line" | awk '{print $1}')
            local util=$(echo "$line" | awk '{print $NF}')
            local await=$(echo "$line" | awk '{print $(NF-2)}')
            if (( $(echo "${util} > ${THRESH_DISK_UTIL}" | bc -l 2>/dev/null || echo 0) )); then
                log_alert "CRITICAL" "DISK-IO" "Device ${dev} I/O utilization at ${util}% (threshold: ${THRESH_DISK_UTIL}%)" \
                    "High disk I/O on ${dev}. Notify Application/Oracle team if DB datafiles reside here."
            fi
            if (( $(echo "${await} > ${THRESH_DISK_AWAIT}" | bc -l 2>/dev/null || echo 0) )); then
                log_alert "WARNING" "DISK-LATENCY" "Device ${dev} await at ${await}ms (threshold: ${THRESH_DISK_AWAIT}ms)" \
                    "High disk latency. Possible storage bottleneck. Engage Storage/VMware team."
            fi
        done
    else
        echo "  [SKIPPED] iostat not available (install sysstat)"
    fi
    echo ""
}

#=============================================================================
# SECTION 6: FILESYSTEM UTILIZATION
#=============================================================================
collect_filesystem_stats() {
    section_header "6" "FILESYSTEM UTILIZATION"

    echo "  >> Local & SAN Filesystems:"
    printf "     %-35s %8s %8s %8s %6s %s\n" "FILESYSTEM" "SIZE" "USED" "AVAIL" "USE%" "MOUNT"
    echo "     ---------------------------------------------------------------------------------"

    df -hP -x tmpfs -x devtmpfs -x nfs -x nfs4 -x fuse.s3fs 2>/dev/null | tail -n +2 | sort -k5 -rn | while read fs size used avail pct mount; do
        local pct_num=${pct%%%}
        local flag=""
        if (( pct_num >= THRESH_FS )); then
            flag=" *** ALERT ***"
            log_alert "CRITICAL" "FILESYSTEM" "Filesystem ${mount} (${fs}) at ${pct} (threshold: ${THRESH_FS}%)" \
                "Filesystem nearing capacity. Identify large files/logs. Extend via LVM or cleanup."
        fi
        printf "     %-35s %8s %8s %8s %6s %s%s\n" "$fs" "$size" "$used" "$avail" "$pct" "$mount" "$flag"
    done

    echo ""
    echo "  >> Inode Utilization (>50%):"
    df -iP -x tmpfs -x devtmpfs -x nfs -x nfs4 2>/dev/null | tail -n +2 | while read fs total used free pct mount; do
        local pct_num=${pct%%%}
        if (( pct_num > 50 )); then
            printf "     %-35s %6s %s\n" "$fs" "$pct" "$mount"
            if (( pct_num >= THRESH_FS )); then
                log_alert "WARNING" "INODE" "Inode usage on ${mount} at ${pct}" \
                    "High inode usage - excessive small files. Identify and cleanup."
            fi
        fi
    done
    echo ""
}

#=============================================================================
# SECTION 7: NFS HEALTH
#=============================================================================
collect_nfs_health() {
    section_header "7" "NFS HEALTH"

    local nfs_mounts
    nfs_mounts=$(mount -t nfs,nfs4 2>/dev/null)
    if [[ -z "$nfs_mounts" ]]; then
        echo "     No NFS mounts detected."
        echo ""
        return
    fi

    echo "  >> NFS Mount Points:"
    printf "     %-40s %-20s %-8s %s\n" "SOURCE" "MOUNT" "TYPE" "OPTIONS"
    echo "     ---------------------------------------------------------------------------------"
    mount -t nfs,nfs4 2>/dev/null | while read src on mnt type fstype opts; do
        printf "     %-40s %-20s %-8s %s\n" "$src" "$mnt" "$fstype" "${opts:0:40}"
    done

    echo ""
    echo "  >> NFS Mount Responsiveness (5s timeout):"
    mount -t nfs,nfs4 2>/dev/null | awk '{print $3}' | while read mnt; do
        local start_ts=$(date +%s%N)
        if timeout 5 stat "$mnt" &>/dev/null; then
            local end_ts=$(date +%s%N)
            local latency_ms=$(( (end_ts - start_ts) / 1000000 ))
            printf "     %-30s : RESPONSIVE (%dms)\n" "$mnt" "$latency_ms"
            if (( latency_ms > 3000 )); then
                log_alert "WARNING" "NFS-SLOW" "NFS mount ${mnt} slow (${latency_ms}ms)" \
                    "NFS latency issue. Engage Network/Storage team."
            fi
        else
            printf "     %-30s : *** HUNG/TIMEOUT ***\n" "$mnt"
            log_alert "CRITICAL" "NFS-HUNG" "NFS mount ${mnt} HUNG or not responding" \
                "IMMEDIATE: NFS mount hung. Check NFS server, network. May need umount -f."
        fi
    done

    echo ""
    echo "  >> NFS Filesystem Utilization:"
    df -hP -t nfs -t nfs4 2>/dev/null | tail -n +2 | while read fs size used avail pct mount; do
        local pct_num=${pct%%%}
        local flag=""
        if (( pct_num >= THRESH_FS )); then
            flag=" *** ALERT ***"
            log_alert "WARNING" "NFS-SPACE" "NFS mount ${mount} at ${pct}" \
                "NFS share nearing capacity. Coordinate with NFS server admin."
        fi
        printf "     %-35s %8s %8s %8s %6s %s%s\n" "$fs" "$size" "$used" "$avail" "$pct" "$mount" "$flag"
    done

    if check_command nfsstat; then
        echo ""
        echo "  >> NFS RPC Statistics:"
        nfsstat -c 2>/dev/null | head -15 | sed 's/^/     /'
    fi

    echo ""
    echo "  >> Stale NFS Handle Check:"
    local stale_count=$(dmesg 2>/dev/null | grep -ci "stale.*nfs\|nfs.*stale" || echo 0)
    if (( stale_count > 0 )); then
        echo "     *** ${stale_count} stale NFS handle message(s) in dmesg ***"
        log_alert "WARNING" "NFS-STALE" "${stale_count} stale NFS handle errors" \
            "Stale handles detected. Remount may be required."
    else
        echo "     No stale NFS handles."
    fi
    echo ""
}

