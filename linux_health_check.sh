#=============================================================================
# EMAIL ALERTING
#=============================================================================
send_email_alert() {
    if [[ "${ENABLE_EMAIL_ALERT}" != "yes" ]]; then return; fi
    if [[ ! -s "${ALERT_LOG}" ]]; then return; fi
    if ! check_command mail && ! check_command mailx && ! check_command sendmail; then
        echo "WARNING: No mail command available. Email alert skipped." >&2
        return
    fi

    local critical_count warning_count
    critical_count=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    warning_count=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)

    local subject="${EMAIL_SUBJECT_PREFIX} ${HOSTNAME_S}: ${critical_count} CRITICAL, ${warning_count} WARNING - ${DATE_STAMP}"
    local body=""
    body+="Linux Health Check Alert - ${HOSTNAME_S}
"
    body+="Generated: ${TIME_STAMP}
"
    body+="OS: ${OS_VERSION}
"
    body+="============================================
"
    body+=""

    while IFS='|' read -r severity component message recommendation; do
        body+="[${severity}] ${component}: ${message}
"
        body+="  --> ${recommendation}
"
        body+=""
    done < "${ALERT_LOG}"

    body+="
Full reports: ${TXT_FILE} | ${HTML_FILE}
"

    # Try sending email
    if check_command mailx; then
        echo "$body" | mailx -s "$subject" -a "${HTML_FILE}" "${EMAIL_RECIPIENTS}" 2>/dev/null
    elif check_command mail; then
        echo "$body" | mail -s "$subject" "${EMAIL_RECIPIENTS}" 2>/dev/null
    fi
}

#=============================================================================
# SALT EVENT PUBLISHING
#=============================================================================
send_salt_event() {
    if [[ "${ENABLE_SALT_EVENT}" != "yes" ]]; then return; fi
    if [[ ! -s "${ALERT_LOG}" ]]; then return; fi
    if ! check_command salt-call; then
        echo "WARNING: salt-call not available. Salt event skipped." >&2
        return
    fi

    local critical_count warning_count
    critical_count=$(grep -c "^CRITICAL" "${ALERT_LOG}" 2>/dev/null || echo 0)
    warning_count=$(grep -c "^WARNING" "${ALERT_LOG}" 2>/dev/null || echo 0)

    # Build JSON payload
    local alerts_json="["
    local first=1
    while IFS='|' read -r severity component message recommendation; do
        (( first )) || alerts_json+=","
        first=0
        # Escape quotes in strings
        message=$(echo "$message" | sed 's/"/\\"/g')
        recommendation=$(echo "$recommendation" | sed 's/"/\\"/g')
        alerts_json+="{\"severity\":\"${severity}\",\"component\":\"${component}\",\"message\":\"${message}\",\"recommendation\":\"${recommendation}\"}"
    done < "${ALERT_LOG}"
    alerts_json+="]"

    local payload="{\"hostname\":\"${HOSTNAME_S}\",\"timestamp\":\"${TIME_STAMP}\",\"os\":\"${OS_VERSION}\",\"critical_count\":${critical_count},\"warning_count\":${warning_count},\"alerts\":${alerts_json},\"txt_report\":\"${TXT_FILE}\",\"html_report\":\"${HTML_FILE}\"}"

    # Fire Salt event
    salt-call event.send "${SALT_EVENT_TAG}" "${payload}" --local 2>/dev/null || \
    salt-call event.fire_master "${payload}" "${SALT_EVENT_TAG}" 2>/dev/null || \
    echo "WARNING: Failed to publish Salt event." >&2
}

#=============================================================================
# CLEANUP OLD REPORTS (keep 30 days)
#=============================================================================
cleanup_old_reports() {
    find "${REPORT_DIR}" -name "*_health_*.txt" -mtime +30 -delete 2>/dev/null
    find "${REPORT_DIR}" -name "*_health_*.html" -mtime +30 -delete 2>/dev/null
    find "${REPORT_DIR}" -name ".alert_findings_*.tmp" -mtime +2 -delete 2>/dev/null
}

#=============================================================================
# MAIN EXECUTION
#=============================================================================
main() {
    echo "$(date): Starting Linux Health Check on ${HOSTNAME_S}" >&2

    # Collect all sections into TXT report
    {
        collect_system_info
        collect_cpu_stats
        collect_memory_stats
        collect_swap_stats
        collect_disk_io
        collect_filesystem_stats
        collect_nfs_health
        collect_s3fs_stats
        collect_historical_stats
        generate_recommendations
    } | tee "${TXT_FILE}"

    # Generate HTML from TXT
    generate_html_report "$(cat "${TXT_FILE}")"

    # Alerting
    send_email_alert
    send_salt_event

    # Cleanup
    cleanup_old_reports

    echo "" >&2
    echo "$(date): Health Check Complete" >&2
    echo "  TXT Report : ${TXT_FILE}" >&2
    echo "  HTML Report: ${HTML_FILE}" >&2

    # Return exit code based on findings
    if grep -q "^CRITICAL" "${ALERT_LOG}" 2>/dev/null; then
        exit 2
    elif grep -q "^WARNING" "${ALERT_LOG}" 2>/dev/null; then
        exit 1
    fi
    exit 0
}

main "$@"
